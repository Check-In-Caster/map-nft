import { prisma } from "./prisma";

function capitalizeFirstLetter(string: string) {
  return string.charAt(0).toUpperCase() + string.slice(1);
}

function stringStartsWithEnglishLetter(string: string) {
  return /^[A-Za-z]/.test(string);
}

type AddressComponents = {
  longText: string;
  shortText: string;
  types: string[];
  languageCode: "en";
}[];

const getPlaceDetailsFromPlaceId = async (placeId: string | undefined) => {
  if (!placeId) return undefined;

  try {
    const response = await fetch(
      `https://places.googleapis.com/v1/places/${placeId}?fields=adrFormatAddress,addressComponents,formattedAddress,location,types&languageCode=en&key=${process.env.MAPS_API_KEY}`
    );

    if (!response.ok) throw new Error("Failed to fetch place details");
    const data = await response.json();

    if (!data) return undefined;
    return data as {
      types: string[];
      formattedAddress: string;
      location: { latitude: number; longitude: number };
      adrFormatAddress: string;
      addressComponents: AddressComponents;
    };
  } catch (error) {
    console.log(error);
  }

  return undefined;
};

const getLocationName = (
  name: string | undefined,
  metadataTitle: string,
  address: string
) => {
  const metadataName = metadataTitle.split(",")[0];
  let locationName = metadataName.split(address)[0].trim();

  if (locationName.includes("·"))
    locationName = locationName.split("·")[0].trim();

  if (locationName.endsWith("."))
    locationName = locationName.split(".")[0].trim();

  if (!name || (address.includes(name) && locationName.length > 3))
    return locationName;

  return name;
};

const getCityFromAddress = (address: string) => {
  try {
    const locality = address
      .split('<span class="locality">')[1]
      .split("</span>")[0];

    const region = address
      .split('<span class="region">')[1]
      .split("</span>")[0];

    if (!locality || !region) return undefined;

    if (locality) return `${locality}, ${region}`;
    else return region;
  } catch {
    try {
      const region = address
        .split('<span class="region">')[1]
        .split("</span>")[0];

      return region;
    } catch {
      try {
        const locality = address
          .split('<span class="locality">')[1]
          .split("</span>")[0];

        return locality;
      } catch {
        return undefined;
      }
    }
  }
};

const getCountryFromAddress = (address: string) => {
  try {
    const country = address
      .split('<span class="country-name">')[1]
      .split("</span>")[0];

    return country;
  } catch {
    return undefined;
  }
};

const getRating = (metadataDescription: string) => {
  const ratingFromDesc = metadataDescription.split("★").length - 1;

  if (ratingFromDesc > 0) return ratingFromDesc;
  return undefined;
};

const getCategory = (description: string, placeTypes: string[]) => {
  try {
    const category = description.split(" · ")[1];
    if (category && stringStartsWithEnglishLetter(category)) return category;
    else if (placeTypes)
      return capitalizeFirstLetter(placeTypes[0].split("_").join(" "));
  } catch {}

  return undefined;
};

const getTypeFromComponent = (component: AddressComponents[number]) => {
  if (component.types.includes("sublocality")) {
    if (component.types.includes("sublocality_level_1"))
      return "sublocality_level_1";
    if (component.types.includes("sublocality_level_2"))
      return "sublocality_level_2";
    if (component.types.includes("sublocality_level_3"))
      return "sublocality_level_3";
    if (component.types.includes("sublocality_level_4"))
      return "sublocality_level_4";
    if (component.types.includes("sublocality_level_5"))
      return "sublocality_level_5";
    return "sublocality";
  }
  if (component.types.includes("locality")) return "locality";
  if (component.types.includes("administrative_area_level_1"))
    return "administrative_area_level_1";
  if (component.types.includes("administrative_area_level_2"))
    return "administrative_area_level_2";
  if (component.types.includes("administrative_area_level_3"))
    return "administrative_area_level_3";
  if (component.types.includes("administrative_area_level_4"))
    return "administrative_area_level_4";
  if (component.types.includes("administrative_area_level_5"))
    return "administrative_area_level_5";
  if (component.types.includes("country")) return "country";
};

const getPlaceTypeFromAddressComponents = (
  locationName: string,
  addressComponents: AddressComponents,
  category: string[]
) => {
  const categoryCheck = category.some((category) => {
    category = category.toLowerCase();
    if (
      category.includes("locality") ||
      category.includes("administrative area") ||
      category.includes("administrative_area") ||
      category.includes("country")
    ) {
      return true;
    }

    return false;
  });

  if (!categoryCheck) return "property";

  const filteredComponents: AddressComponents = [];
  const sortedComponents: AddressComponents = [];

  for (const component of addressComponents) {
    const type = getTypeFromComponent(component);
    if (type) filteredComponents.push(component);
  }

  const typesInOrder = [
    "country",
    "administrative_area_level_1",
    "administrative_area_level_2",
    "administrative_area_level_3",
    "administrative_area_level_4",
    "administrative_area_level_5",
    "locality",
    "sublocality_level_1",
    "sublocality_level_2",
    "sublocality_level_3",
    "sublocality_level_4",
    "sublocality_level_5",
  ];

  for (const type of typesInOrder) {
    for (const component of filteredComponents) {
      if (getTypeFromComponent(component) === type) {
        sortedComponents.push(component);
      }
    }
  }

  // if length is 1, it's definitely a country
  if (sortedComponents.length === 1) return "country";

  // comparing text with location name
  for (let i = 0; i < sortedComponents.length; i++) {
    const component = sortedComponents[i];

    if (component.longText.toLowerCase().includes(locationName.toLowerCase())) {
      if (i === 0) return "country";
      if (i === 1) return "state";
      if (i === 2) return "city";
      if (i === 3) return "town";
    }
  }

  // if text doesn't match, we'll have to rely on the number of components
  if (sortedComponents.length === 2) return "state";
  if (sortedComponents.length === 3) return "city";
  if (sortedComponents.length === 4) return "town";
  return "property";
};

const getFormattedPlaceDetails = ({
  name,
  metadataTitle,
  adrAddress,
  metadataDescription,
  types,
}: {
  name?: string;
  metadataTitle: string;
  adrAddress: string;
  metadataDescription: string;
  types: string[];
}) => {
  const location = getLocationName(name, metadataTitle, adrAddress);
  const city = getCityFromAddress(adrAddress);
  const country = getCountryFromAddress(adrAddress);
  const rating = getRating(metadataDescription);
  const category = getCategory(metadataDescription, types);

  return {
    location,
    city,
    country,
    rating,
    category,
  };
};

const searchArray = (array: string[], search: string) => {
  for (const el of array) {
    if (search.toLowerCase().includes(el.toLowerCase())) return true;
  }

  return false;
};

const getPoints = (category: string, rating: number, noOfReviews: number) => {
  const w1 = 0.2;
  const w2 = 0.6;
  const w3 = 0.2;

  let c = 0;
  if (searchArray(excludedCategories, category)) return 0;
  if (c == 0 && searchArray(tier1Categories, category)) c = 1;
  if (c == 0 && searchArray(tier2Categories, category)) c = 0.8;
  if (c == 0 && searchArray(tier3Categories, category)) c = 0.6;
  if (c == 0 && searchArray(tier4Categories, category)) c = 0.4;

  const lambda = 300;

  const score = Math.round(
    10000 * (w1 * rating + w2 * (1 - Math.exp(-noOfReviews / lambda)) + w3 * c)
  );

  return score >= 0 ? score : 0;
};

const getPointsFromPlaceId = async (placeId: string) => {
  if (!placeId) return undefined;

  try {
    const response = await fetch(
      `https://places.googleapis.com/v1/places/${placeId}?fields=rating,userRatingCount,types&languageCode=en&key=${process.env.MAPS_API_KEY}`
    );

    if (!response.ok) throw new Error("Failed to fetch place details");
    const data = (await response.json()) as {
      types: string[];
      rating: number;
      userRatingCount: number;
    } | null;

    if (!data) return undefined;

    const locationRecord = await prisma.locations.findFirst({
      where: {
        place_id: placeId,
      },
    });

    const category = locationRecord?.category
      ? locationRecord.category
      : capitalizeFirstLetter(data.types[0].split("_").join(" "));

    const points = getPoints(category, data.rating, data.userRatingCount);

    return points;
  } catch (error) {
    console.log(error);
  }

  return undefined;
};

const getPropertyScore = async (placeId: string, type: string) => {
  if (type === "country") return 1000000;
  else if (type === "state") return 100000;
  else if (type === "city") return 40000;
  else if (type === "town") return 20000;
  else return await getPointsFromPlaceId(placeId);
};

